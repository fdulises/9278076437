	public static function create(ContainerInterface $container) {
		$session = $container->get('gv_fplus.session');
		$order = $container->get('gv_fanatics_plus_order.order');
		$documents = $container->get('gv_fanatics_plus_order.documents');
		
		return new static($session, $documents, $order);
	}
	
	public function __construct($session, $documents, $order) {
		$this->session = $session;
		$this->documents = $documents;
		$this->order = $order;
	}

	public function onDownload(RouteMatchInterface $route_match) {
		$orderID = $route_match->getParameter('orderID');
		$documentType = $route_match->getParameter('documentType');
		
		if (!is_numeric($orderID)) {
			throw new NotFoundHttpException();
		}
		
		$order = $this->order->getFromID($orderID, TRUE, TRUE);
		if (!isset($order)) {
			throw new NotFoundHttpException();
		}
		
		$currentUserID = $this->session->getIDUser();
		if ($order->IDUser != $currentUserID) { // Not the owner
			throw new AccessDeniedHttpException();
		}
		
		$documents = $this->documents->getFromOrderID($orderID);
		
		$selectedDocument = array_pop(array_reverse(array_filter($documents, function ($document) use ($documentType) { return $document->IDDocumentType == $documentType && $document->Exists == TRUE; })));
		if (!isset($selectedDocument)) {
			throw new NotFoundHttpException();
		}
		
		$format = $selectedDocument->Format;
		$filename = $selectedDocument->Name;
		$base64 = base64_decode ($selectedDocument->DocumentToBase64);
				
		// Generate response
		$response = new Response();
		$mimeType = $this->_ext2mime($format);
				
		// Set headers
		$response->headers->set('Cache-Control', 'private');
		$response->headers->set('Content-type', $mimeType );
		$response->headers->set('Content-Disposition', 'attachment; filename="' . $filename . '";');
		$response->headers->set('Content-length',  strlen($base64));
		
		// Send headers before outputting anything
		$response->sendHeaders();
		
		$response->setContent( $base64 );
		
		return $response;
	}

